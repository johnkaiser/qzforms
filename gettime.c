#include <sys/time.h>
#include <time.h>

/*
 * Return the time as a float for easy interval
 * arithmetic.
 */

double gettime(void){
    struct timespec ts;
    double t;

    clock_gettime(CLOCK_REALTIME, &ts);
    t = ts.tv_sec;
    t += ((double)ts.tv_nsec)*0.000000001;

    return t;
}

